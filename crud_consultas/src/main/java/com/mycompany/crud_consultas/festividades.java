    void consultar() {
        String sql = "SELECT * FROM festividades;";

        try {
            conet = con.getConection();
            st = conet.createStatement();
            rs = st.executeQuery(sql);
            Object[] fest = new Object[4];
            modelo = (DefaultTableModel) tabla.getModel();

            while (rs.next()) {
                fest[0] = rs.getInt("id_holiday");
                fest[1] = rs.getString("holiday_date");
                fest[2] = rs.getString("holiday_name");
                fest[3] = rs.getString("ods_related");

                modelo.addRow(fest);
            }
            tabla.setModel(modelo);
        } catch (Exception e) {
            System.out.println("error: " + e);
        }
    }

    void agregar() {
        String fech = fecha.getText();
        String nom = nombre.getText();
        String od = (String) ods.getSelectedItem();

        try {
            if (nom.equals("") || od.equals("") || fech.equals("")) {
                JOptionPane.showMessageDialog(null, "Faltan datos");
            } else {
                String sql = "INSERT INTO festividades (holiday_date, holiday_name, ods_related) VALUES" + "('" + fech + "', '" + nom + "', '" + od + "');";
                conet = con.getConection();
                st = conet.createStatement();
                st.executeUpdate(sql);
                JOptionPane.showMessageDialog(null, "Agregado con éxito");
                limpiarTabla();
            }
        } catch (Exception e) {
            System.out.println("Error: " + e.getMessage());
        }
    }

    void limpiarTabla() {
        while (modelo.getRowCount() > 0) {
            modelo.removeRow(0);
        }
    }

    void eliminar() {
        int fila = tabla.getSelectedRow();

        try {
            if (fila < 0) {
                JOptionPane.showMessageDialog(null, "esta fila no fue seleccionada");
                limpiarTabla();
            } else {
                String sql = "delete from festividades where id_holiday=" + idc;
                conet = con.getConection();
                st = conet.createStatement();
                st.executeUpdate(sql);
                JOptionPane.showMessageDialog(null, "Eliminado con exito");
                limpiarTabla();
            }

        } catch (Exception e) {
            System.out.println("error: " + e);
        }
    }

    void Modificar() {
        int fila = tabla.getSelectedRow();
        idc = Integer.parseInt((String) tabla.getValueAt(fila, 0).toString());
        String fech = fecha.getText();
        String nom = nombre.getText();
        String od = (String) ods.getSelectedItem();
        
        try {
            if (nom.equals("") || od.equals("") || fech.equals("")) {
                JOptionPane.showMessageDialog(null, "Faltan datos");
            } else {
                String sql = "UPDATE festividades SET holiday_date = '" + fech + "', holiday_name = '" + nom + "', ods_related = '" + od + "' where id_holiday = '" + idc + "';";
                conet = con.getConection();
                st = conet.createStatement();
                st.executeUpdate(sql);
                JOptionPane.showMessageDialog(null, "Modificado con exito");
                limpiarTabla();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
        }
    }
    
