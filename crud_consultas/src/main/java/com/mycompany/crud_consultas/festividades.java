    private void tablaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaMouseClicked
        int fila = tabla.getSelectedRow();
        if (fila == -1) {
            JOptionPane.showMessageDialog(null, "No se selecciono fila");
        } else {
            idc = Integer.parseInt((String) tabla.getValueAt(fila, 0).toString());
            String fech = (String) tabla.getValueAt(fila, 1);
            String nom = (String) tabla.getValueAt(fila, 2);
            String od = (String) tabla.getValueAt(fila, 3);

            id.setText("" + idc);
            fecha.setText(fech);
            nombre.setText(nom);
            ods.setSelectedItem(od);
        }
    }//GEN-LAST:event_tablaMouseClicked

