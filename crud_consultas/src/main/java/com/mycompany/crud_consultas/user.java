void consultar(){
   String sql = "SELECT * FROM user;";
   
    try {
        conet = con.getConection();
        st = conet.createStatement();
        rs = st.executeQuery(sql);
        Object[] user = new Object[9];
        modelo = (DefaultTableModel) tabla.getModel();
        
        while (rs.next()) {
            user [0] = rs.getInt("id_user");
            user [1] = rs.getString("name");
            user [3] = rs.getString("email");
            user [2] = rs.getString("last_name");
            user [4] = rs.getString("phone_number");
            user [5] = rs.getString("reason");
            user [6] = rs.getString("verification");
            user [7] = rs.getDate("date");
            user [8] = rs.getString("state");
            
            modelo.addRow(user);
        }
        tabla.setModel(modelo);
    } catch (Exception e) {
        System.out.println("error: " + e);
    }
}

void agregar(){
    String nom = nombre.getText();
    String ape = apellido.getText(); 
    String cor = correo.getText();
    String tel = telefono.getText();
    String mot = motivo.getText();
    String ver = (String) verificacion.getSelectedItem();
    String sta = (String) estado.getSelectedItem();
    LocalDate fecha = LocalDate.now();
    
    try {
        if (nom.equals("") || ape.equals("") || cor.equals("") || tel.equals("") || mot.equals("")){
            JOptionPane.showMessageDialog(null,"Faltan datos");
        } else {
            String sql = "INSERT INTO user (name, last_name, email, phone_number, reason, verification, date, state) VALUES " +
             "('" + nom + "', '" + ape + "', '" + cor + "', '" + tel + "', '" + mot + "', '" + ver + "', '" + fecha + "', '" + sta + "')";
            conet = con.getConection();
            st = conet.createStatement();
            st.executeUpdate(sql);
            JOptionPane.showMessageDialog(null, "Agregado con éxito");
            limpiarTabla();
        }
    } catch (Exception e) {
        System.out.println("Error: " + e.getMessage());
    }
}

void limpiarTabla() {
    while (modelo.getRowCount() > 0) {
        modelo.removeRow(0);
    }
}

