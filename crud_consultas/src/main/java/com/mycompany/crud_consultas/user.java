    private void tablaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaMouseClicked
        int fila = tabla.getSelectedRow();
        if (fila == -1) {
           JOptionPane.showMessageDialog(null, "No se selecciono fila");
        } else {
            idc = Integer.parseInt((String) tabla.getValueAt(fila, 0) .toString());
            String nom = (String) tabla.getValueAt(fila, 1);
            String ape = (String) tabla.getValueAt(fila, 2);
            String cor = (String) tabla.getValueAt(fila, 3);
            String tel = (String) tabla.getValueAt(fila, 4);
            String res = (String) tabla.getValueAt(fila, 5);
            String ver = (String) tabla.getValueAt(fila, 6);
            String est = (String) tabla.getValueAt(fila, 8);
            
            id.setText(""+ idc);
            nombre.setText(nom);
            apellido.setText(ape);
            correo.setText(cor);
            telefono.setText(tel);
            motivo.setText(res);
            verificacion.setSelectedItem(ver);
            estado.setSelectedItem(est);
        }
    }//GEN-LAST:event_tablaMouseClicked

